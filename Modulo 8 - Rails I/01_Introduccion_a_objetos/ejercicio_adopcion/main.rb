
##* Ejercicio: Adopción de Mascotas
# Se busca realizar un software donde podamos relacionar la adopción de mascotas con su nueva familia.
# Para esto necesitamos registrar el nombre de la persona encargada, un teléfono y una dirección dónde estará
# el animal. Por el lado del animal, debemos tener un nombre, un tipo (si es gato o perro), si está vacunado o no
# y la edad.
# Implementar las siguientes funcionalidades:
# 1. Se debería poder asignar una mascota a una familia
# 2. Una familia debería poder mostrar todos los nombres de las mascotas que han adoptado
# 3. Las mascotas deberían poder mostrar su propia información
# 4. Si las familias todavía no han adoptado a ninguna mascota, debería lanzarse una excepción con un mensaje "Sin mascotas adoptadas"